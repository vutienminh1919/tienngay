    <p class="MsoNormal" style="margin-top: 0pt; margin-right: 0in; margin-bottom: 0in; margin-left: 0in;">
      <b><span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> MỤC I. QUY ĐỊNH CHUNG </span></b>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 0in;">
      <b><span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> Điều 1. Định nghĩa </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.1. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> “Số Tiền Vay”: là số tiền mà Bên Cho Vay đồng ý cho Bên Vay vay và tiến hành giải ngân cho Bên Vay thông qua VFC. </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.2. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> “Ngày Giải Ngân”: là ngày Bên Cho Vay hoặc Bên ủy quyền của Bên Cho Vay chuyển toàn bộ Số Tiền Vay cho Bên Vay. </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.3. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Số Tiền Giải Ngân”: là số tiền thực tế Bên Cho Vay và/hoặc VFC  chuyển khoản cho Bên Vay sau khi trừ đi Phí Bảo Hiểm theo quy định tại Thỏa Thuận này.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.4. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Lãi Suất”: là lãi suất áp dụng cho Số Tiền Vay theo một tỷ lệ được quy định tại Thỏa Thuận này. Lãi Suất được tính theo năm với cơ sở một năm có 365 ngày.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.5. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Tiền Lãi”: là số tiền lãi được tính dựa trên dư nợ thực tế của Số Tiền Vay, được tính dựa trên Lãi Suất và số ngày vay thực tế đối với phần Số Tiền Vay chưa thanh toán kể từ Ngày Giải Ngân.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.6. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Phí Trả Nợ Trước Hạn”: là khoản phí Bên Vay phải trả cho Bên Cho Vay, áp dụng tại mọi thời điểm Bên Vay thanh toán vượt quá số tiền gốc phải trả theo lịch trả nợ và/hoặc Thông Báo. Số tiền vượt quá đó được coi là Nợ Gốc
        Trả Trước Hạn.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.7. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Phí Quản Lý Số Tiền Vay Chậm Trả”: là khoản phí Bên Vay phải trả cho VFC, áp dụng trong trường hợp Bên Vay không thanh toán đúng hạn các nghĩa vụ cần thanh toán phát sinh theo quy định tại Thỏa Thuận này.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.8. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Phí Thẩm Định và Lưu Trữ Tài Sản Đảm Bảo”: là khoản phí Bên Vay phải trả cho VFC, áp dụng trọng trường hợp có Tài Sản Đảm Bảo và VFC được Bên Cho Vay chỉ định nhận, quản lý và bảo quản Tài Sản Đảm Bảo.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.9. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Phí Tư Vấn Gia Hạn Số Tiền Vay”: là khoản phí Bên Vay phải trả cho VFC, áp dụng trong trường hợp Bên Vay có nhu cầu gia hạn thời hạn vay Số Tiền Vay theo Thỏa Thuận này.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.10. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> ‘Dịch Vụ Tư Vấn Quản Lý”: là các dịch vụ tư vấn quản lý mà VFC cung cấp cho Bên Vay theo quy định tại Thỏa Thuận này. </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.11. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Phí Tư Vấn Quản Lý”: là khoản phí mà Bên Vay phải trả cho VFC để VFC cung cấp Dịch Vụ Tư Vấn Quản Lý cho Bên Vay. Khoản Phí này được thanh toán hàng tháng.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.12. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Số Tiền Phải Thanh Toán”: là tổng số tiền mà Bên Vay phải thanh toán đúng hạn cho Bên Cho Vay theo Thỏa Thuận, bao gồm nhưng không giới hạn Số Tiền Vay, tiền lãi và các chi phí khác phát sinh.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.13. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Thông Báo”: là bất kỳ thông báo hoặc cách thể hiện nào cung cấp thông tin từ Bên Cho Vay/VFC đến Bên Vay liên quan đến việc thực hiện Thỏa Thuận này. Thông Báo có thể thể hiện dưới dạng tin nhắn điện thoại hoặc email
        hoặc văn bản tùy từng trường hợp.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.14. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> “Bên Bảo Hiểm”: là Tổng Công ty cổ phần Bảo Hiểm Toàn cầu, cung cấp cho Bên Vay sản phẩm bảo hiểm tai nạn người vay. </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 1.15. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        “Phí Bảo Hiểm”: là khoản tiền Bên Vay phải trả khi mua bảo hiểm liên quan đến Khoản Vay của Bên Bảo Hiểm thông qua VFC, bằng cách chỉ định cho VFC khấu trừ Phí Bảo Hiểm từ Khoản Vay tại Ngày Giải Ngân để thanh toán cho
        Bên Bảo Hiểm.
      </span>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 0in; text-align: justify;">
      <b><span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> Điều 2. Mục đích của Thỏa Thuận </span></b>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 0in; text-align: justify;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        Các Bên đồng ý rằng, dựa trên sự tư vấn của VFC, Bên Cho Vay cho Bên Vay vay Số Tiền Vay. Bên Vay đồng ý đồng thời mua bảo hiểm liên quan đến Khoản Vay của Bên Bảo Hiểm thông qua VFC với quy định cụ thể tại Thỏa Thuận
        này. Theo đó, mối quan hệ độc lập giữa hai trong Các Bên tại Thỏa Thuận này được thể hiện như sau:
      </span>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 2.1. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        Bên Cho Vay đồng ý cho Bên Vay vay một khoản tiền bằng Số Tiền Vay. Quy định chi tiết về Số Tiền Vay tại Mục II Điều Khoản Chung này.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 2.2. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> Bên Vay chỉ định sử dụng Dịch Vụ Tư Vấn Quản Lý của VFC theo quy định chi tiết tại Mục III Điều Khoản Chung này. </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 2.3. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        Bên Cho Vay chỉ định VFC đại diện Bên Cho Vay nhận, bảo quản Tài Sản Đảm Bảo (nếu có) và các giấy tờ liên quan đến Tài Sản Đảm Bảo từ Bên Vay. Thỏa thuận về việc hợp tác này chi tiết tại hợp đồng hợp tác giữa hai bên.
      </span>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 0in; text-align: justify;">
      <b><span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> MỤC II. THỎA THUẬN GIỮA BÊN CHO VAY VÀ BÊN VAY </span></b>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 0in; text-align: justify;">
      <b><span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> Điều 3. Thỏa thuận vay </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 3.1. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> Bên Cho Vay đồng ý và Bên Vay đồng ý vay một số tiền tương ứng Số Tiền Vay từ Bên Cho Vay. </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 3.2. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> Tiền lãi được tính theo công thức sau: </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> Tiền lãi = ∑ (Dư nợ thực tế từng thời điểm của Số Tiền Vay x Lãi Suất/365 x Số ngày vay tương ứng Dư nợ thực tế từng thời điểm) </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 3.3. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> Nghĩa vụ thanh toán: Bên Vay có nghĩa vụ thanh toán đầy đủ Số Tiền Phải Thanh Toán cho Bên Cho </span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> Vay thông </span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> qua VFC (theo chỉ định của Bên Cho Vay). Bên Vay thanh toán theo tờ Hướng dẫn thanh toán đính kèm Thỏa Thuận này. </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 3.4. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        Gia hạn Số Tiền Vay: Trường hợp Bên Vay muốn gia hạn Số Tiền Vay thì Bên Vay phải gửi đề nghị gia hạn cho VFC để VFC thông báo cho Bên Cho Vay. Số Tiền Vay chỉ được gia hạn khi có sự chấp thuận bằng Thông Báo đến Bên Vay
        của Bên Cho Vay.
      </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 3.5. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        Phí Trả Nợ Trước Hạn: Tại bất kỳ thời điểm nào của Thỏa Thuận, Bên Vay có thể trả trước một phần hoặc toàn bộ Số Tiền Vay kèm phí trả nợ trước hạn bằng […]% Nợ Gốc Trả Trước Hạn. Phí Trả Nợ Trước Hạn được thanh toán cùng
        thời điểm Bên Vay thanh toán Số Tiền Vay trước hạn cho Bên Cho Vay.
      </span>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 3pt; margin-left: 0.45pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 4. Quyền và nghĩa vụ của Bên Cho Vay </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 106%;">
      <span style="font-size: 5pt; line-height: 106%; font-family: 'Arial', sans-serif;"> 4. 1. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 106%; font-family: 'Arial', sans-serif;">
        Quyết định việc giải ngân Số Tiền Vay theo đề nghị của Bên Vay thông qua cách thức mà Bên Cho Vay cho là phù hợp tùy từng thời điểm. Khi thực hiện các công việc liên quan đến giải ngân Số Tiền Vay, Bên Cho Vay/Bên được
        ủy quyền của Bên Cho Vay được toàn quyền ghi âm lại bất kỳ nội dung nào trao đổi qua điện thoại giữa Bên Cho Vay và Khách Hàng, và Khách Hàng tại đây thừa nhận và đồng ý rằng dữ liệu ghi âm đó là bằng chứng chứng minh
        việc Khách Hàng cam kết thực hiện các nghĩa vụ liên quan đến Thỏa Thuận này.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 106%;">
      <span style="font-size: 5pt; line-height: 106%; font-family: 'Arial', sans-serif;"> 4. 2. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 106%; font-family: 'Arial', sans-serif;">
        Tiền lãi Bên Cho Vay được hưởng là tiền lãi chưa bao gồm thuế, Bên cho vay tự thực hiện kê khai và nộp thuê thu nhập cá nhân theo đúng quy định của pháp luật.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 106%;">
      <span style="font-size: 5pt; line-height: 106%; font-family: 'Arial', sans-serif;"> 4. 3. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 106%; font-family: 'Arial', sans-serif;"> Yêu cầu Bên Vay phải thanh toán đầy đủ và đúng hạn Số Tiền Phải Thanh Toán theo Thỏa Thuận này. </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 106%;">
      <span style="font-size: 5pt; line-height: 106%; font-family: 'Arial', sans-serif;"> 4. 4. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 106%; font-family: 'Arial', sans-serif;"> Tuân thủ các quy định đã cam kết trong Thỏa Thuận. </span>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 3pt; margin-left: 0.45pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 5. Quyền và nghĩa vụ của Bên Vay </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 112%;">
      <span style="font-size: 5pt; line-height: 112%; font-family: 'Arial', sans-serif;"> 5.1. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 112%; font-family: 'Arial', sans-serif;">
        Được đề nghị gia hạn Số Tiền Vay với điều kiện thanh toán đầy đủ và đúng hạn tiền lãi và các khoản phí phát sinh từ Thỏa Thuận này theo Thông Báo.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 112%;">
      <span style="font-size: 5pt; line-height: 112%; font-family: 'Arial', sans-serif;"> 5.2. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 112%; font-family: 'Arial', sans-serif;"> Thực hiện đầy đủ nghĩa vụ thanh toán khi đến hạn theo quy định tại Thỏa Thuận. </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 112%;">
      <span style="font-size: 5pt; line-height: 112%; font-family: 'Arial', sans-serif;"> 5.3. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 112%; font-family: 'Arial', sans-serif;"> Tuân thủ các quy định của pháp luật hiện hành liên quan đến việc thực hiện các nghĩa vụ của Bên Vay. </span>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 3pt; margin-left: 0.45pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 6. Sự Kiện Vi Phạm </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 102%;">
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> 6. 1. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> Mỗi sự kiện hoặc trường hợp trong các sự kiện/ trường hợp quy định sau đây được xem là một Sự Kiện Vi Phạm: </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 117%;">
      <span style="font-size: 5pt; line-height: 117%; font-family: 'Arial', sans-serif;"> a. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp;&nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 117%; font-family: 'Arial', sans-serif;"> Bên Vay không tuân thủ bất kỳ quy định nào của Thỏa Thuận này hoặc Thông Báo hoặc văn bản, tài liệu khác liên quan đến Thỏa Thuận; </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 117%;">
      <span style="font-size: 5pt; line-height: 117%; font-family: 'Arial', sans-serif;"> b. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp;&nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 117%; font-family: 'Arial', sans-serif;"> Bên Vay cung cấp các thông tin hoặc giấy tờ liên quan đến Số Tiền Vay không đúng, không chính xác, không trung thực; </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 117%;">
      <span style="font-size: 5pt; line-height: 117%; font-family: 'Arial', sans-serif;"> c. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp;&nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 117%; font-family: 'Arial', sans-serif;"> Bên Vay không thanh toán đúng hạn bất kỳ khoản đến hạn nào trong Số Tiền Phải Thanh Toán. </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 102%;">
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> 6. 2. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> Ngay khi và sau khi xảy ra một Sự Kiện Vi Phạm, Bên Cho Vay có quyền: </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 102%;">
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> a. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp;&nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> Tuyên bố đến hạn thanh toán ngay Số Tiền Phải Thanh Toán; </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 102%;">
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> b. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp;&nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> Thu giữ và tiến hành xử lý Tài Sản Bảo Đảm (nếu có) theo quy định tại Thỏa Thuận; </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 102%;">
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> c. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp;&nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> Chấm dứt tất cả các nghĩa vụ của Bên Cho Vay đối với Bên Vay theo Thỏa Thuận; </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 102%;">
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> d. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp;&nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 102%; font-family: 'Arial', sans-serif;"> Thực hiện tất cả các quyền và chế tài hợp lý của Bên Cho Vay theo quy định tại Thỏa Thuận này. </span>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 37pt; margin-bottom: 3pt; margin-left: 0.45pt; line-height: 110%;">
      <b><span style="font-size: 5pt; line-height: 110%; font-family: 'Arial', sans-serif;"> Điều 7. Thỏa thuận về biện pháp bảo đảm </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> 7.1. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;">
        Bên Vay và Bên Cho Vay đồng ý lựa chọn biện pháp bảo đảm là cầm cố tài sản thuộc sở hữu của Bên Vay để đảm bảo nghĩa vụ thanh toán Số Tiền Vay của Bên Vay đối với Bên Cho Vay <b> (“Tài Sản Đảm Bảo”) </b> .
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> 7.2. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;">
        Sau khi Bên Cho Vay đã giải ngân Số Tiền Vay cho Bên Vay, Bên Vay chuyển giao Tài Sản Đảm Bảo và các giấy tờ liên quan đến Tài Sản Đảm Bảo (bản gốc) cho Bên Cho Vay hoặc cho bên được Bên Cho Vay chỉ định. Trong Thời Hạn
        Vay, Bên Cho Vay hoặc bên được Bên Cho Vay chỉ định giữ Tài Sản Đảm Bảo cho Bên Vay.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> 7.3. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;">
        Trong trường hợp Tài Sản Đảm Bảo là xe máy, ô tô hoặc các phương tiện di chuyển cá nhân khác, Bên Vay có thể giữ lại Tài Sản Đảm Bảo để sử dụng (nếu có nhu cầu) và sẽ bàn giao lại toàn bộ bản gốc các giấy tờ liên quan
        đến Tài Sản Đảm Bảo cho Bên Cho Vay.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> 7.4. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;">
        Bên Cho Vay sẽ chuyển giao lại Tài Sản Đảm Bảo cùng các giấy tờ mà Bên Cho Vay đã nhận cho Bên Vay khi Bên Vay hoàn thành tất cả nghĩa vụ tài chính theo Thỏa Thuận này hoặc Bên Vay có nhu cầu thay thế một Tài Sản Đảm Bảo
        khác cho Số Tiền Vay.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> 7.5. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Xử lý Tài Sản Bảo Đảm: </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 0in; margin-right: 0in; margin-bottom: 3pt; margin-left: 14.2pt; text-align: justify;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;">
        Ngay khi và sau khi xảy ra một Sự Kiện Vi Phạm (bao gồm nhưng không giới hạn: chậm thực hiện, thực hiện không đúng, không đầy đủ nghĩa vụ thanh toán,…) được quy định tại Thỏa Thuận nàyVFC  có toàn quyền xử lý Tài Sản Bảo
        Đảm theo quy định của pháp luật
      </span>
      <span lang="VI" style="font-size: 5pt; font-family: 'Arial', sans-serif;"> ( </span><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> như Tự bán tài sản bảo đảm </span>
      <span lang="VI" style="font-size: 5pt; font-family: 'Arial', sans-serif;"> ,...) </span><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> để trừ vào Số Tiền Phải Thanh Toán của Bên Vay. </span>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 3pt; margin-left: 0.45pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 8. Cam kết của Bên Vay về Tài Sản Bảo Đảm </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 97%;">
      <span style="font-size: 5pt; line-height: 97%; font-family: 'Arial', sans-serif;"> 8.1. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 97%; font-family: 'Arial', sans-serif;">
        Bên Vay cam kết tại thời điểm ký Thỏa Thuận này, Tài Sản Bảo Đảm thuộc quyền sở hữu hợp pháp của Bên Vay, có nguồn gốc hợp pháp, không bị cơ quan nhà nước có thẩm quyền xử lý theo quy định của pháp luật, đang không bị
        tranh chấp, thế chấp, cầm cố, bảo lãnh với bên thứ ba nào.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 97%;">
      <span style="font-size: 5pt; line-height: 97%; font-family: 'Arial', sans-serif;"> 8.2. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 97%; font-family: 'Arial', sans-serif;">
        Cam kết vô điều kiện và không hủy ngang việc chịu trách nhiệm bồi thường thiệt hại cho Bên Cho Vay hoặc bên thứ ba trong trường hợp phát sinh bất kỳ thiệt hại, kiện đòi, mất mát, hư tổn, chi phí nào liên quan đến Tài Sản
        Bảo Đảm và việc giao Tài Sản Bảo Đảm; và đồng ý rằng Bên Cho Vay được loại trừ khỏi tất cả các trách nhiệm nêu trên.
      </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 0in; margin-right: 0in; margin-bottom: 3pt; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 97%;">
      <span style="font-size: 5pt; line-height: 97%; font-family: 'Arial', sans-serif;"> 8.3. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 97%; font-family: 'Arial', sans-serif;">
        Cam kết cung cấp và/hoặc bổ sung và/hoặc xuất trình bằng chứng về quyền sở hữu của Bên Vay đối với Tài Sản Bảo Đảm và/hoặc nguồn gốc của Tài Sản Bảo Đảm theo yêu cầu của Bên Cho Vay; Cam kết bổ sung, thay thế bất kỳ tài
        sản nào khác Tài Sản Bảo Đảm để bảo đảm nghĩa vụ thanh toán Số Tiền Vay theo Thỏa Thuận tại bất kỳ thời điểm nào mà Bên Cho Vay cho là cần thiết.
      </span>
    </p>
    <p class="MsoNormal" style="margin-top: 0in; margin-right: 0in; margin-bottom: 3pt; margin-left: 0.45pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> MỤC III. THỎA THUẬN GIỮA BÊN VAY VÀ VFC </span></b>
    </p>
    <p class="MsoNormal" style="margin-top: 0in; margin-right: 0in; margin-bottom: 3pt; margin-left: 0.45pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 9. Dịch Vụ Tư Vấn Quản Lý </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: normal;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> 9.1. <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Bên Vay chỉ định và VFC đồng ý cung cấp Dịch Vụ Tư Vấn Quản Lý với nội dung cụ thể của Dịch Vụ như sau: </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -7.1pt; line-height: normal;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> a. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Tư Vấn Quản Lý; </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -7.1pt; line-height: normal;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> b. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Thẩm định và lưu trữ Tài Sản Đảm Bảo/giầy tờ của Tài Sản Đảm Bảo; </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -7.1pt; line-height: normal;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> c. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Quản lý khoản vay </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 14.2pt; text-align: justify; text-indent: -7.1pt; line-height: normal;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> d. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Các dịch vụ khác theo thỏa thuận giữa Bên Vay và VFC nếu Bên Vay có nhu cầu. </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 0in; margin-right: 0in; margin-bottom: 0in; margin-left: 0.25in; text-align: justify; text-indent: -0.25in; line-height: normal;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> 9.2 <span style="font: 7pt 'Times New Roman';"> &nbsp;&nbsp;&nbsp;&nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;">
        Bên Vay sẽ thanh toán Phí Tư Vấn Quản Lý, Phí Quản Lý Số Tiền Vay Chậm Trả (nếu có) cho VFC theo quy định tại Thỏa Thuận này và chính sách cụ thể của VFC.
      </span>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 3pt; margin-left: 0in; text-align: justify;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 10. Phí Tư Vấn Quản Lý và Phí Quản Lý Số Tiền Vay Chậm Trả </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 108%;">
      <span style="font-size: 5pt; line-height: 108%; font-family: 'Arial', sans-serif;"> 10.1. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 108%; font-family: 'Arial', sans-serif;">
        Phí Tư Vấn Quản Lý đối với Số Tiền Vay được quy định tại Thỏa Thuận này đã bao gồm VAT. Bên Vay và VFC đồng ý rằng Phí Tư Vấn Quản Lý cụ thể sẽ được quy định cụ thể tại Thông Báo Đến Bên Vay theo chính sách phí do VFC áp
        dụng tại từng thời điểm.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 108%;">
      <span style="font-size: 5pt; line-height: 108%; font-family: 'Arial', sans-serif;"> 10.2. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 108%; font-family: 'Arial', sans-serif;"> Điều kiện thu Phí Tư Vấn Quản Lý: Phí Tư Vấn Quản Lý được thu khi Số Tiền Vay được giải ngân thành công cho Bên Vay. </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 108%;">
      <span style="font-size: 5pt; line-height: 108%; font-family: 'Arial', sans-serif;"> 10.3. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 108%; font-family: 'Arial', sans-serif;">
        Thanh toán Phí Tư Vấn Quản Lý: Bên Vay thanh toán Phí Tư Vấn Quản Lý hàng tháng cho VFC. Phí Tư Vấn Quản Lý sẽ được VFC tính vào Số Tiền Phải Thanh Toán của Bên Vay hàng tháng và Bên Vay sẽ tiến hành thanh toán vào ngày
        được nêu tại Thông Báo của VFC.
      </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 0in; margin-right: 0in; margin-bottom: 3pt; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 108%;">
      <span style="font-size: 5pt; line-height: 108%; font-family: 'Arial', sans-serif;"> 10.4. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 108%; font-family: 'Arial', sans-serif;">
        Phí Quản Lý Số Tiền Vay Chậm Trả: Phí Quản Lý Số Tiền Vay Chậm Trả được tính là […] cho mỗi lần Bên Vây thanh toán không đúng hạn Số Tiền Phải Thanh Toán quy định tại Thỏa Thuận này. Phí Quản Lý Số Tiền Vay Chậm Trả sẽ
        áp dụng cho đến khi Bên Vay hoàn thành các nghĩa vụ thanh toán.
      </span>
    </p>
    <p class="MsoNormal" style="margin-bottom: 3pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> &nbsp; </span></b>
    </p>
    <p class="MsoNormal" style="margin-bottom: 3pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> MỤC IV. QUY ĐỊNH KHÁC </span></b>
    </p>
    <p class="MsoNormal" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 3pt; margin-left: 0in; text-align: justify;">
      <b><span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> Điều 11. Thông Báo đến Bên Vay </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 3pt; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 11.1. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        Bên Vay tại đây xác nhận đồng ý chịu sự ràng buộc và tuân thủ các Thông Báo đến Bên Vay trong mọi trường hợp. Bất kỳ Thông Báo nào được gửi bằng hình thức tin nhắn đến số điện thoại đã đăng ký của Bên Vay sẽ được coi là
        đã gửi và Bên Vay đã nhận được.
      </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 3pt; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 11.2. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        Thông Báo là phần không thể tách rời của Thỏa Thuận này. Trong trường hợp Thông Báo vì lý do nào đó có một hoặc một vài thông tin bị lỗi và/hoặc sai sót, VFC và/hoặc Bên Cho Vay được quyền đính chính, chỉnh sửa thông tin
        đó bằng việc gửi một Thông Báo khác đến Bên Vay.
      </span>
    </p>
    <p class="MsoNormal" style="margin-bottom: 3pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 12. Thuế, Phí </span></b>
    </p>
    <p class="MsoNormal" style="margin-bottom: 3pt; text-align: justify;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;">
        Mỗi Bên sẽ tự chịu trách nhiệm kê khai, nộp các khoản thuế, phí phát sinh thuộc trách nhiệm của mình liên quan đến việc thực hiện Thỏa Thuận này tại cơ quan nhà nước có thẩm quyền.
      </span>
    </p>
    <p class="MsoNormal" style="margin-bottom: 3pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 13. Sự kiện bất khả kháng </span></b>
    </p>
    <p class="MsoNormal" style="margin-bottom: 3pt; text-align: justify; line-height: 119%;">
      <span style="font-size: 5pt; line-height: 119%; font-family: 'Arial', sans-serif;">
        Sự kiện bất khả kháng là sự kiện xảy ra một cách khách quan không thể lường trước được và không thể khắc phục được mặc dù đã áp dụng mọi biện pháp cần thiết và khả năng cho phép. Trường hợp Bên có nghĩa vụ không thực
        hiện nghĩa vụ do sự kiện bất khả kháng thì không phải bồi thường thiệt hại đối với Bên có quyền, trừ trường hợp pháp luật có quy định khác. Bên có nghĩa vụ phải lập tức thông báo ngay cho Bên có quyền biết về việc không
        thực hiện được nghĩa vụ do sự kiện bất khả kháng. Trường hợp sự kiện bất khả kháng không ảnh hưởng đối với Bên nào thì Bên đó vẫn phải tiếp tục thực hiện các nghĩa vụ, cam kết của mình đối với Bên có quyền theo quy định
        tại Thỏa Thuận này.
      </span>
    </p>
    <p class="MsoNormal" style="margin-bottom: 3pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 14. Bồi thường thiệt hại, Phạt vi phạm </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 115%; background: white;">
      <span style="font-size: 5pt; line-height: 115%; font-family: 'Arial', sans-serif;"> 14.1. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 115%; font-family: 'Arial', sans-serif; color: black;">
        Trừ khi xảy ra Sự kiện bất khả kháng theo Thỏa Thuận này, trường hợp một Bên không thực hiện, thực hiện không đúng, không đầy đủ bất kỳ nghĩa vụ nào đến hạn theo quy định tại Thỏa Thuận, Bên vi phạm ngay lập tức phải
        chấm dứt vi phạm của mình, khắc phục những hậu quả do hành vi vi phạm trong một thời gian nhất định được yêu cầu bởi Bên bị vi phạm, đồng thời chịu phạt với mức tối đa 8% (tám phần trăm) giá trị phần nghĩa vụ bị vi phạm;
      </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 0in; margin-right: 0in; margin-bottom: 3pt; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 115%; background: white;">
      <span style="font-size: 5pt; line-height: 115%; font-family: 'Arial', sans-serif;"> 14.2. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 115%; font-family: 'Arial', sans-serif; color: black;"> Trường hợp có thiệt hại, Bên vi phạm phải bồi thường toàn bộ thiệt hại phát sinh cho Bên bị vi phạm. </span>
    </p>
    <p class="MsoNormal" style="margin-bottom: 3pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 15. Luật áp dụng, giải quyết tranh chấp </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-top: 0in; margin-right: 0in; margin-bottom: 3pt; margin-left: 14.2pt; text-indent: -14.2pt;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> 15.1. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Thỏa Thuận này được điều chỉnh theo quy định của pháp luật Việt Nam. </span>
    </p>
    <p class="MsoListParagraphCxSpLast" style="margin-top: 0in; margin-right: 0in; margin-bottom: 3pt; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> 15.2. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; font-family: 'Arial', sans-serif;">
        Bất kỳ tranh chấp nào phát sinh từ hoặc liên quan đến Thỏa Thuận này trước tiên phải được giải quyết trên cơ sở thương lượng, hòa giải. Nếu không giải quyết được bằng thương lượng, hòa giải, Các Bên có thể đưa tranh chấp
        ra Tòa án có thẩm quyền của Việt Nam để giải quyết, án phí do bên thua chịu trách nhiệm chi trả.
      </span>
    </p>
    <p class="MsoNormal" style="margin-bottom: 3pt;">
      <b><span style="font-size: 5pt; font-family: 'Arial', sans-serif;"> Điều 16. Các điều khoản khác </span></b>
    </p>
    <p class="MsoListParagraphCxSpFirst" style="margin-top: 3pt; margin-right: 0in; margin-bottom: 3pt; margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt;">
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;"> 11.3. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 107%; font-family: 'Arial', sans-serif;">
        VFC được quyền gửi thông báo tới khách hàng đã và đang sử dụng dịch vụ của VFC thông tin về sản phẩm hay bất kể dịch vụ nào của VFC thông qua các phương tiện truyền tải thông tin, bao gồm nhưng không giới hạn: tin nhắn,
        cuộc gọi, thư điện tử,…
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 119%;">
      <span style="font-size: 5pt; line-height: 119%; font-family: 'Arial', sans-serif;"> 16.1. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 119%; font-family: 'Arial', sans-serif;">
        Bên Vay cam kết và thừa nhận rằng bất kỳ thông tin nào do Bên Vay cung cấp trong quá trình giao kết và thực hiện Thỏa Thuận là chính xác, trung thực. Bên Vay có trách nhiệm thông báo cho Bên Cho Vay và VFC ngay khi có
        thay đổi về bất kỳ thông tin nào của Bên Vay.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 119%;">
      <span style="font-size: 5pt; line-height: 119%; font-family: 'Arial', sans-serif;"> 16.2. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 119%; font-family: 'Arial', sans-serif;">
        Bên Vay bằng văn bản này đồng ý và cho phép Bên Cho Vay và/hoặc VFC được sử dụng, mã hóa, truyền tải, lưu trữ; chuyển giao và tiết lộ bất cứ thông tin nào liên quan đến Bên Vay và/hoặc do Bên Vay cung cấp và/hoặc vấn đề,
        tài liệu liên quan đến Thỏa Thuận này cho bất kỳ bên thứ ba nào tại bất cứ thời điểm nào mà Bên Cho Vay và/hoặc VFC cho là phù hợp và cần thiết trong phạm vi pháp luật cho phép mà không phải có thêm một sự chấp thuận nào
        khác của Bên Vay, đồng thời không phải chịu trách nhiệm với Bên Vay liên quan đến các hành vi này.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 120%;">
      <span style="font-size: 5pt; line-height: 120%; font-family: 'Arial', sans-serif;"> 16.3. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span lang="VI" style="font-size: 5pt; line-height: 120%; font-family: 'Arial', sans-serif;">
        Toàn bộ các quyền, nghĩa vụ của VFC và Bên cho vay được hai bên đảm bảo thực thiện theo quy định chi tiết tại Hợp đồng hợp tác giữa hai bên.
      </span>
    </p>
    <p class="MsoListParagraphCxSpMiddle" style="margin-left: 14.2pt; text-align: justify; text-indent: -14.2pt; line-height: 120%;">
      <span style="font-size: 5pt; line-height: 120%; font-family: 'Arial', sans-serif;"> 16.4. <span style="font: 7pt 'Times New Roman';"> &nbsp; </span></span>
      <span style="font-size: 5pt; line-height: 120%; font-family: 'Arial', sans-serif;">
        Trong quá trình thực hiện Thỏa Thuận, Các Bên có thể yêu cầu sửa đổi, bổ sung Thỏa Thuận. Việc sửa đổi, bổ sung Thỏa Thuận này phải được Các Bên đồng ý và lập thành văn bản dưới hình thức phụ lục và phải được xác nhận
